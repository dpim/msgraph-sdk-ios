#import "comODataEntities.h"
#import "comModels.h"
#import "MSURLSessionDataTask.h"





@interface MSRequest()

@property NSMutableArray *options;

- (NSMutableURLRequest *)requestWithMethod:(NSString *)method
                                      body:(NSData *)body
                                   headers:(NSDictionary *)headers;

@end

@interface comWorkbookFunctionsFixedRequest()


@property (nonatomic, getter=number) comJson * number;


@property (nonatomic, getter=decimals) comJson * decimals;


@property (nonatomic, getter=noCommas) comJson * noCommas;

@end

@implementation comWorkbookFunctionsFixedRequest


- (instancetype)initWithNumber:(comJson *)number decimals:(comJson *)decimals noCommas:(comJson *)noCommas URL:(NSURL *)url options:(NSArray *)options client:(ODataBaseClient*)client
{
    self = [super initWithURL:url options:options client:client];
    if (self){
        _number = number;
        _decimals = decimals;
        _noCommas = noCommas;
    }
    return self;
}

- (NSMutableURLRequest *)mutableRequest
{
    NSDictionary *params = [[NSDictionary alloc] initWithObjectsAndKeys:[MSObject getNSJsonSerializationCompatibleValue:_number],@"number",[MSObject getNSJsonSerializationCompatibleValue:_decimals],@"decimals",[MSObject getNSJsonSerializationCompatibleValue:_noCommas],@"noCommas",nil];


    NSData *body = [NSJSONSerialization dataWithJSONObject:params options:0 error:nil];
    return [self requestWithMethod:@"POST" body:body headers:nil];
}


- (MSURLSessionDataTask *)executeWithCompletion:(void (^)(comWorkbookFunctionResult *response, NSError *error))completionHandler
{

    MSURLSessionDataTask *task = [self taskWithRequest:self.mutableRequest
                                odObjectWithDictionary:^(id responseObject){
                                                           return [[comWorkbookFunctionResult alloc] initWithDictionary:responseObject];
                                                       }
                                            completion:completionHandler];
    [task execute];
    return task;
}

@end
