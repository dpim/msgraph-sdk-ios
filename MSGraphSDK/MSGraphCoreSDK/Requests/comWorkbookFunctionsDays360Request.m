#import "comODataEntities.h"
#import "comModels.h"
#import "MSURLSessionDataTask.h"





@interface MSRequest()

@property NSMutableArray *options;

- (NSMutableURLRequest *)requestWithMethod:(NSString *)method
                                      body:(NSData *)body
                                   headers:(NSDictionary *)headers;

@end

@interface comWorkbookFunctionsDays360Request()


@property (nonatomic, getter=startDate) comJson * startDate;


@property (nonatomic, getter=endDate) comJson * endDate;


@property (nonatomic, getter=method) comJson * method;

@end

@implementation comWorkbookFunctionsDays360Request


- (instancetype)initWithStartDate:(comJson *)startDate endDate:(comJson *)endDate method:(comJson *)method URL:(NSURL *)url options:(NSArray *)options client:(ODataBaseClient*)client
{
    self = [super initWithURL:url options:options client:client];
    if (self){
        _startDate = startDate;
        _endDate = endDate;
        _method = method;
    }
    return self;
}

- (NSMutableURLRequest *)mutableRequest
{
    NSDictionary *params = [[NSDictionary alloc] initWithObjectsAndKeys:[MSObject getNSJsonSerializationCompatibleValue:_startDate],@"startDate",[MSObject getNSJsonSerializationCompatibleValue:_endDate],@"endDate",[MSObject getNSJsonSerializationCompatibleValue:_method],@"method",nil];


    NSData *body = [NSJSONSerialization dataWithJSONObject:params options:0 error:nil];
    return [self requestWithMethod:@"POST" body:body headers:nil];
}


- (MSURLSessionDataTask *)executeWithCompletion:(void (^)(comWorkbookFunctionResult *response, NSError *error))completionHandler
{

    MSURLSessionDataTask *task = [self taskWithRequest:self.mutableRequest
                                odObjectWithDictionary:^(id responseObject){
                                                           return [[comWorkbookFunctionResult alloc] initWithDictionary:responseObject];
                                                       }
                                            completion:completionHandler];
    [task execute];
    return task;
}

@end
