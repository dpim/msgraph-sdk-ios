#import "comODataEntities.h"
#import "comModels.h"
#import "MSURLSessionDataTask.h"





@interface MSRequest()

@property NSMutableArray *options;

- (NSMutableURLRequest *)requestWithMethod:(NSString *)method
                                      body:(NSData *)body
                                   headers:(NSDictionary *)headers;

@end

@interface comWorkbookFunctionsAverageIfRequest()


@property (nonatomic, getter=range) comJson * range;


@property (nonatomic, getter=criteria) comJson * criteria;


@property (nonatomic, getter=averageRange) comJson * averageRange;

@end

@implementation comWorkbookFunctionsAverageIfRequest


- (instancetype)initWithRange:(comJson *)range criteria:(comJson *)criteria averageRange:(comJson *)averageRange URL:(NSURL *)url options:(NSArray *)options client:(ODataBaseClient*)client
{
    self = [super initWithURL:url options:options client:client];
    if (self){
        _range = range;
        _criteria = criteria;
        _averageRange = averageRange;
    }
    return self;
}

- (NSMutableURLRequest *)mutableRequest
{
    NSDictionary *params = [[NSDictionary alloc] initWithObjectsAndKeys:[MSObject getNSJsonSerializationCompatibleValue:_range],@"range",[MSObject getNSJsonSerializationCompatibleValue:_criteria],@"criteria",[MSObject getNSJsonSerializationCompatibleValue:_averageRange],@"averageRange",nil];


    NSData *body = [NSJSONSerialization dataWithJSONObject:params options:0 error:nil];
    return [self requestWithMethod:@"POST" body:body headers:nil];
}


- (MSURLSessionDataTask *)executeWithCompletion:(void (^)(comWorkbookFunctionResult *response, NSError *error))completionHandler
{

    MSURLSessionDataTask *task = [self taskWithRequest:self.mutableRequest
                                odObjectWithDictionary:^(id responseObject){
                                                           return [[comWorkbookFunctionResult alloc] initWithDictionary:responseObject];
                                                       }
                                            completion:completionHandler];
    [task execute];
    return task;
}

@end
